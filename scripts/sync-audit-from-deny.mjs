#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────────
// Sync `src-tauri/.cargo/audit.toml` from `src-tauri/deny.toml` (#11).
//
// `cargo audit` (run as a warn-only step in `_validate.yml`) does NOT
// consult `deny.toml`, so every advisory we've already triaged and
// accepted in `[advisories].ignore` re-surfaces in the CI log,
// drowning out the signal we actually care about (new advisories
// since deny.toml was last triaged).
//
// `cargo audit` reads its own ignore list from `<crate>/.cargo/audit.toml`
// (locally verified with cargo-audit 0.22.1: a file at `<crate>/audit.toml`
// is silently ignored, only the `.cargo/`-prefixed path is honoured). To
// keep `deny.toml` as the single source of truth and avoid drift, this
// script projects the `[advisories].ignore` entries from deny.toml
// (objects with `id` + `reason`) into the string-array form that
// audit.toml requires.
//
// Idempotent: running twice produces no diff.
// Wired into `prek.toml` as the `audit-toml-in-sync` hook, which
// re-runs this script and fails if the working copy of audit.toml
// drifts from what the script would emit (= the user forgot to
// re-sync after editing deny.toml).
//
// Deliberately uses a focused inline parser instead of a full TOML
// library: keeps the script dependency-free (smol-toml only ships
// here as a transitive of knip/markdownlint-cli2 via overrides;
// importing it directly would surface as a missing dependency).
// The narrow shape we read — `[advisories].ignore = [ { id = "...",
// reason = "..." }, ... ]` — is stable enough that a regex over the
// section body is the cheapest reliable approach. If deny.toml's
// schema changes, the assertions below fail loudly.
// ─────────────────────────────────────────────────────────────────────
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const REPO_ROOT = resolve(__dirname, '..')
const DENY_PATH = resolve(REPO_ROOT, 'src-tauri/deny.toml')
const AUDIT_PATH = resolve(REPO_ROOT, 'src-tauri/.cargo/audit.toml')

const HEADER = `# AUTO-GENERATED by scripts/sync-audit-from-deny.mjs from src-tauri/deny.toml. Do not edit manually — run \`node scripts/sync-audit-from-deny.mjs\` after editing deny.toml.
#
# cargo-audit reads its ignore list from \`<crate>/.cargo/audit.toml\`
# (verified locally with cargo-audit 0.22.1: a file at
# \`<crate>/audit.toml\` is silently ignored). The  #11 plan
# named \`src-tauri/audit.toml\`; the script writes here instead so
# the file is actually honoured by cargo-audit.
`

function extractIgnoreIds(denyTomlText) {
  const headerIdx = denyTomlText.indexOf('[advisories]')
  if (headerIdx === -1) {
    throw new Error('deny.toml is missing [advisories] table')
  }
  // Slice up to the next top-level section header so we don't pick up
  // `id = ...` entries from elsewhere (e.g. [bans], [licenses]).
  const after = denyTomlText.slice(headerIdx + '[advisories]'.length)
  const nextSectionMatch = after.match(/^\s*\[/m)
  const sectionBody = nextSectionMatch ? after.slice(0, nextSectionMatch.index) : after

  const arrayMatch = sectionBody.match(/ignore\s*=\s*\[([\s\S]*?)\]/)
  if (!arrayMatch) {
    throw new Error('deny.toml is missing [advisories].ignore array')
  }
  const arrayBody = arrayMatch[1]

  // Match either `{ id = "..." ... }` object form or a bare `"..."` string form.
  const ids = []
  const objectIdRegex = /id\s*=\s*"([^"]+)"/g
  let m
  while ((m = objectIdRegex.exec(arrayBody)) !== null) {
    ids.push(m[1])
  }

  if (ids.length === 0) {
    throw new Error('deny.toml [advisories].ignore parsed to 0 entries')
  }
  return ids
}

function renderAuditToml(ids) {
  // cargo-audit accepts string IDs only (not deny.toml's object form).
  // Emit a vertical array so a future drift surfaces line-by-line in
  // git blame / PR diffs, matching the deny.toml hand-formatted style.
  const lines = []
  lines.push(HEADER)
  lines.push('[advisories]')
  lines.push('ignore = [')
  for (const id of ids) {
    lines.push(`    "${id}",`)
  }
  lines.push(']')
  lines.push('')
  return lines.join('\n')
}

function main() {
  const args = process.argv.slice(2)
  const checkOnly = args.includes('--check')

  const denyText = readFileSync(DENY_PATH, 'utf8')
  const ids = extractIgnoreIds(denyText)
  const out = renderAuditToml(ids)

  let existing = null
  try {
    existing = readFileSync(AUDIT_PATH, 'utf8')
  } catch {
    // file does not exist yet — first run
  }

  if (existing === out) {
    // No change. Stay silent on idempotent re-runs so the
    // `audit-toml-in-sync` prek hook produces minimal noise.
    return
  }

  if (checkOnly) {
    // Used by the `audit-toml-in-sync` prek hook. Don't mutate
    // the working tree — just report and fail with a clear
    // remediation pointer.
    console.error('ERROR: src-tauri/.cargo/audit.toml is out of sync with src-tauri/deny.toml.')
    console.error('Run `node scripts/sync-audit-from-deny.mjs` and stage the result.')
    process.exit(1)
  }

  mkdirSync(dirname(AUDIT_PATH), { recursive: true })
  writeFileSync(AUDIT_PATH, out)
  console.log(
    `wrote src-tauri/.cargo/audit.toml (${ids.length} advisor${ids.length === 1 ? 'y' : 'ies'} synced from deny.toml)`,
  )
}

main()
