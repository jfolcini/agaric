#!/usr/bin/env node
/**
 * Build-time generator for the full categorized emoji dataset (#286 sub-issue 1).
 *
 * Reads the vetted `emojibase-data` package (a devDependency only — it never
 * ships at runtime) and emits a trimmed, grouped, committed TypeScript module
 * at `src/editor/emoji-data.generated.ts`. The runtime (`src/editor/emoji-data.ts`)
 * imports that module, so the app carries one static data blob and no new
 * runtime dependency — keeping the picker local-first and offline.
 *
 * Trimming: per emoji we keep only the native glyph, a canonical shortcode
 * (`name`), a small set of search keywords (extra shortcodes + tags), and a
 * skin-tone flag. The nine standard CLDR groups are preserved in order; the
 * "Component" group (skin-tone / hair modifiers) and ungrouped codepoints
 * (e.g. bare regional indicators) are dropped.
 *
 * Regenerate with:  npm run gen:emoji
 */

import { writeFileSync } from 'node:fs'
import { createRequire } from 'node:module'
import { resolve } from 'node:path'

const require = createRequire(import.meta.url)
/** @type {Array<Record<string, unknown>>} */
const data = require('emojibase-data/en/data.json')
/** @type {Record<string, string | string[]>} */
const shortcodes = require('emojibase-data/en/shortcodes/emojibase.json')

const here = import.meta.dirname
const OUT = resolve(here, '../src/editor/emoji-data.generated.ts')

/** Display names for the standard CLDR groups, in render order. "Component"
 *  (group 2) is intentionally absent — those are modifiers, not pickable. */
const GROUP_NAMES = {
  0: 'Smileys & Emotion',
  1: 'People & Body',
  3: 'Animals & Nature',
  4: 'Food & Drink',
  5: 'Travel & Places',
  6: 'Activities',
  7: 'Objects',
  8: 'Symbols',
  9: 'Flags',
}
const GROUP_ORDER = [0, 1, 3, 4, 5, 6, 7, 8, 9]

/** Max search keywords kept per emoji — caps the generated blob size. */
const MAX_KEYWORDS = 6

const stripVS = (s) => s.replace(/️/g, '')

/** A base emoji is "naive-tonable" when appending the light Fitzpatrick
 *  modifier to the (variation-selector-stripped) base reproduces emojibase's
 *  own light-skin variant. This excludes ZWJ sequences where a naive append
 *  would render wrong, so the runtime's append-based `applySkinTone` stays
 *  correct. */
function isTonable(e) {
  if (!Array.isArray(e.skins)) return false
  const light = e.skins.find((s) => s.tone === 1)
  if (!light) return false
  return `${stripVS(e.emoji)}\u{1F3FB}` === stripVS(light.emoji)
}

/** Normalize a shortcode token to the picker's `[a-z0-9_]+` shape. */
const clean = (s) =>
  String(s)
    .toLowerCase()
    .replace(/[^a-z0-9_]+/g, '_')
    .replace(/^_+|_+$/g, '')

function shortcodesFor(hexcode) {
  const raw = shortcodes[hexcode]
  if (!raw) return []
  return (Array.isArray(raw) ? raw : [raw]).map(clean).filter(Boolean)
}

const usedNames = new Set()

/** Pick a unique canonical name: the first free shortcode, else suffix it. */
function uniqueName(codes, hexcode) {
  for (const c of codes) {
    if (!usedNames.has(c)) {
      usedNames.add(c)
      return c
    }
  }
  const base = codes[0] ?? `e_${hexcode.toLowerCase().replace(/[^a-z0-9]+/g, '_')}`
  let name = base
  let n = 2
  while (usedNames.has(name)) name = `${base}_${n++}`
  usedNames.add(name)
  return name
}

/** group index -> array of trimmed entries */
const buckets = new Map(GROUP_ORDER.map((g) => [g, []]))

for (const e of data) {
  const group = e.group
  if (typeof group !== 'number' || !GROUP_NAMES[group]) continue
  const codes = shortcodesFor(e.hexcode)
  // Prefer a wordy shortcode for the canonical name: "+1" cleans to "1", but
  // 👍 should be `thumbsup`, not `1`. Letter-bearing codes win (stable order);
  // purely-numeric ones (e.g. 💯 → "100") fall back when nothing else exists.
  const ordered = [
    ...codes.filter((c) => /[a-z]/.test(c)),
    ...codes.filter((c) => !/[a-z]/.test(c)),
  ]
  const name = uniqueName(ordered, e.hexcode)
  const keywords = []
  const seen = new Set([name])
  for (const kw of [...codes.filter((c) => c !== name), ...(e.tags ?? []).map(clean)]) {
    if (!kw || seen.has(kw)) continue
    seen.add(kw)
    keywords.push(kw)
    if (keywords.length >= MAX_KEYWORDS) break
  }
  const entry = { c: e.emoji, n: name }
  if (keywords.length > 0) entry.k = keywords
  if (isTonable(e)) entry.s = true
  buckets.get(group).push(entry)
}

const groups = GROUP_ORDER.filter((g) => (buckets.get(g)?.length ?? 0) > 0).map((g) => ({
  group: GROUP_NAMES[g],
  emoji: buckets.get(g),
}))

const total = groups.reduce((sum, g) => sum + g.emoji.length, 0)

const banner = `/**
 * AUTO-GENERATED by scripts/generate-emoji-data.mjs — DO NOT EDIT BY HAND.
 *
 * Source: emojibase-data (devDependency, build-time only). Regenerate with
 * \`npm run gen:emoji\`. ${total} emoji across ${groups.length} CLDR groups.
 *
 * Compact keys keep the blob small: c = native char, n = canonical shortcode,
 * k = extra search keywords, s = accepts a Fitzpatrick skin-tone modifier.
 */
/* oxlint-disable */

export interface GeneratedEmoji {
  readonly c: string
  readonly n: string
  readonly k?: readonly string[]
  readonly s?: boolean
}

export interface GeneratedEmojiGroup {
  readonly group: string
  readonly emoji: readonly GeneratedEmoji[]
}

export const EMOJI_DATA: readonly GeneratedEmojiGroup[] = `

const body = JSON.stringify(groups, null, 0)
writeFileSync(OUT, `${banner}${body} as const\n`)
console.log(`Wrote ${total} emoji (${groups.length} groups) to ${OUT}`)
